// JavaScript supports several data types.
// Here's a list with examples:

// 1. Number:
let age = 25;
let height = 5.9;

// 2. String:
let name = "John";
let message = 'Hello, World!';
let studentId = '1';

// 3. Boolean:
let isStudent = true;
let hasCar = false;

// 4. Object:
let person = {
    name: "Alice",
    age: 30,
    isStudent: false,
    colors: {
        favColor: 'green',
        hateColor: 'pink'
    }
};

// 5. Array:
let fruits = ["apple", "orange", "banana"];

// 6. Null:
let value = null;

// 7. Undefined:
let undefinedValue;

// 8. Symbol:
let id = Symbol("uniqueId");

// 9. BigInt:
let bigNumber = 1234567890123456789012345678901234567890n;

// 10. Function:
function greet(name) {
    return `Hello, ${name}!`;
}